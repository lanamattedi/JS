/** Operadores
 * Adicão ou concatenação (quando é string) = +
 * Subtração = -
 * Divisão = /
 * Multiplicação = *
 * Potenciação = **
 * Resto de divisão = %
 */

const numb01 = 5;
const numb02 = 10;
const numb03 = 2;
const nome = '1';

console.log (numb01 + numb02);
console.log (numb03 % numb02);
console.log (numb03 ** numb01);
console.log (nome + numb01);  

